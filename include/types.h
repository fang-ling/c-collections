#ifndef types_h
#define types_h

#include <stdbool.h>
#include <stdint.h>

/* On both 32-bit and 64-bit platforms, Int should be the same size as Int64 */
typedef int64_t Int;
typedef uint64_t UInt;

typedef int32_t Int32;
typedef uint32_t UInt32;

typedef int8_t Int8;
typedef uint8_t UInt8;

typedef bool Bool;

typedef double Double;

typedef char Char;

#endif /* types_h */
